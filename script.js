/* ══════════════════════════════════════════════════════════════
   AMBIENT CANVAS PARTICLES (new — inspired by teamLab's
   immersive atmosphere; subtle gold dust over the hero)
══════════════════════════════════════════════════════════════ */
function initParticles() {
  const canvas = document.getElementById('heroCanvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');

  function resize() {
    canvas.width  = canvas.offsetWidth;
    canvas.height = canvas.offsetHeight;
  }
  resize();
  window.addEventListener('resize', resize, { passive: true });

  const N = 55;
  const particles = Array.from({ length: N }, () => ({
    x: Math.random() * canvas.width,
    y: Math.random() * canvas.height,
    r: Math.random() * 1.8 + 0.3,
    vx: (Math.random() - 0.5) * 0.22,
    vy: -(Math.random() * 0.35 + 0.1), // drift upward
    alpha: Math.random() * 0.5 + 0.1,
    fade: (Math.random() - 0.5) * 0.004,
  }));

  function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    particles.forEach(p => {
      p.x += p.vx;
      p.y += p.vy;
      p.alpha += p.fade;

      if (p.alpha <= 0.05 || p.alpha >= 0.65) p.fade *= -1;
      if (p.y < -5) { p.y = canvas.height + 5; p.x = Math.random() * canvas.width; }
      if (p.x < -5 || p.x > canvas.width + 5) p.x = Math.random() * canvas.width;

      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = `rgba(201,168,76,${p.alpha.toFixed(2)})`;
      ctx.fill();
    });
    requestAnimationFrame(draw);
  }
  draw();
}

