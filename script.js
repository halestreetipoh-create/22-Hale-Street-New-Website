/* ══════════════════════════════════════════════════════════════
   MULTILINGUAL ENGINE
   
   FIX: The old switchLang() compared b.getAttribute('onclick')
   which was fragile. Now we use data-lang attributes on buttons
   for a clean, unambiguous comparison.
══════════════════════════════════════════════════════════════ */
function applyLang(lang) {
  const t = i18n[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!(key in t)) return;
    const val = t[key];
    if (val && typeof val === 'object' && 'html' in val) {
      el.innerHTML = val.html;
    } else if (val !== undefined && val !== null) {
      el.textContent = val;
    }
  });

  // Update placeholders separately (inputs can't use textContent)
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });

  document.documentElement.lang = lang === 'zh' ? 'zh-Hans' : lang;
}

function switchLang(lang) {
  /* FIX: was comparing textContent to lang.toUpperCase() which broke
     for 'ms' (button says 'BM') and 'zh' (button says '中').
     Now we read data-lang from the button directly. */
  currentLang = lang;

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  applyLang(lang);
  showToast(i18n[lang].toastLang || 'Language changed');
}

/* ══════════════════════════════════════════════════════════════
   SCROLL REVEAL — IntersectionObserver
   
   FIX: Observer was created but observe() was NEVER called.
   We call it inside DOMContentLoaded after DOM is ready.
══════════════════════════════════════════════════════════════ */
const revealObs = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObs.unobserve(entry.target); // Once revealed, stop watching
    }
  });
}, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });

/* ══════════════════════════════════════════════════════════════
   STAT COUNTER ANIMATION
   Inspired by Met Museum and Louvre's impactful stats display.
   Counts up when the strip enters the viewport.
══════════════════════════════════════════════════════════════ */
function animateCounter(el, target, suffix = '') {
  const duration = 1800;
  const start = performance.now();
  const isDecimal = target % 1 !== 0;

  const tick = (now) => {
    const elapsed = now - start;
    const progress = Math.min(elapsed / duration, 1);
    // Ease out cubic
    const eased = 1 - Math.pow(1 - progress, 3);
    const current = Math.round(eased * target);
    el.textContent = (target >= 1000
      ? current.toLocaleString()
      : current) + suffix;
    if (progress < 1) requestAnimationFrame(tick);
  };
  requestAnimationFrame(tick);
}

const statsObs = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (!entry.isIntersecting) return;
    // Find all strip-num elements and animate them
    entry.target.querySelectorAll('.strip-num[data-count]').forEach(el => {
      const target = parseFloat(el.getAttribute('data-count'));
      const suffix = el.getAttribute('data-suffix') || '';
      animateCounter(el, target, suffix);
    });
    statsObs.unobserve(entry.target);
  });
}, { threshold: 0.4 });

